function min(num1, num2) {
  if( num1 < num2){
    return num1;
  }else{
    return num2;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num){
  if(num % 2 === 0){
    return true;
  }return false;
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, letter){
  var counter=0;
  for( var i= 0; i< str.length; i++){
    if(str[i] === letter){
      counter += 1;
    }
  }return counter;
}

console.log(countChars('lololol', 'l'));

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str){
  var counter= 0;
  for( var i=0; i< str.length; i++){
    if(str[i] === 'B'){
      counter += 1;
    }
  }
  return counter;
}

console.log(countBs('BBB'));

